    <h2>Legislation and Guidance</h2>
    <p>Here you can find details of legislation and best practice guidance relating to online social media use.</p>
        
    <h3>Legislation:</h3>
    <ul>
      <li><strong>Children's Online Privacy Protection Act (COPPA):</strong> This U.S. federal law regulates the online collection of personal information from children under 13.</li>
      <li><strong>General Data Protection Regulation (GDPR):</strong> This European Union regulation addresses data protection and privacy concerns, including online data collection.</li>
      <li><strong>Family Educational Rights and Privacy Act (FERPA):</strong> A U.S. federal law that protects the privacy of student education records, including those held online.</li>
      <li><strong>California Consumer Privacy Act (CCPA):</strong> California state law that enhances privacy rights and consumer protection for residents of California.</li>
      <li><strong>EU e-Privacy Directive:</strong> Legislation from the European Union governing the privacy and security of electronic communications.</li>
    </ul>
    <h3>Best Practice Guidance:</h3>
    <ul>
      <li><strong>Think before you share:</strong> Encourage teens to consider the potential consequences before posting anything online, including photos, videos, or personal information.</li>
      <li><strong>Keep personal information private:</strong> Advise teenagers to avoid sharing sensitive details like their full name, address, phone number, or school information on social media platforms.</li>
      <li><strong>Use strong, unique passwords:</strong> Teach teens the importance of using strong and unique passwords for their social media accounts to prevent unauthorized access.</li>
      <li><strong>Be cautious with friend requests:</strong> Remind teenagers to only accept friend requests from people they know and trust in real life.</li>
      <li><strong>Verify before clicking:</strong> Encourage teens to verify the authenticity of links and attachments before clicking on them to prevent phishing attacks or malware infections.</li>
    </ul>
