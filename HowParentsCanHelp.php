    <h2>How Parents Can Help</h2>
    <p>Here are tips that parents can use to support healthy teen use of social media:</p>
    <ul>
      <li><strong>Establish open communication:</strong> Create an environment where your teen feels comfortable discussing their online experiences and concerns.</li>
      <li><strong>Set clear boundaries:</strong> Set reasonable limits on screen time and establish rules about what types of content are appropriate.</li>
      <li><strong>Teach critical thinking:</strong> Help your teen develop critical thinking skills to evaluate the reliability and credibility of information they encounter online.</li>
      <li><strong>Encourage empathy:</strong> Foster empathy and kindness by discussing the impact of online behavior on others and promoting respectful communication.</li>
      <li><strong>Stay informed:</strong> Keep yourself updated on the latest social media trends, apps, and privacy settings to better support your teen.</li>
      <li><strong>Model healthy behavior:</strong> Be a positive role model by demonstrating responsible digital habits and respectful online interactions.</li>
      <li><strong>Monitor online activity:</strong> Monitor your teen's online activity without invading their privacy, and be aware of any signs of cyberbullying or harmful behavior.</li>
      <li><strong>Encourage offline activities:</strong> Encourage your teen to balance their online time with offline activities such as hobbies, sports, or spending time with friends and family.</li>
      <li><strong>Teach digital citizenship:</strong> Educate your teen about their rights and responsibilities as digital citizens, including respecting others' privacy and intellectual property.</li>
      <li><strong>Seek professional help if needed:</strong> If you notice signs of excessive screen time, cyberbullying, or mental health issues, seek guidance from a qualified professional.</li>
    </ul>
